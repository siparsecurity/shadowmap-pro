"""
ShadowMap - Module 4: Vulnerability Hints
Flags common misconfigurations — NOT exploitation, just awareness.
"""

import hashlib
import random
import string
import requests
from rich.console import Console

console = Console()

TIMEOUT = 5

# How different a finding's body length must be from the host's baseline
# ("nothing here") response to count as real, as a fraction of the
# baseline length. Raise this if you get too many false positives on
# hosts with slightly dynamic catch-all pages (timestamps, nonces, etc).
LENGTH_DIFF_THRESHOLD = 0.15

